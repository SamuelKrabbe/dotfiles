draw_ascii() {
    local art="$1"

    local rows=$(tput lines)
    local cols=$(tput cols)

    # Get the longest line
    local max_length=$(echo "$art" | awk '{ if (length > max) max=length } END { print max }')

    # Vertical padding
    local art_lines=$(echo "$art" | wc -l)
    local vpad=$(( (rows - art_lines) / 2 ))

    for ((i=0;i<vpad;i++)); do echo; done

    # Print each line with horizontal padding
    while IFS= read -r line; do
        local hpad=$(( (cols - ${#line}) / 2 ))
        printf "%*s%s\n" "$hpad" "" "$line"
    done <<< "$art"
}

